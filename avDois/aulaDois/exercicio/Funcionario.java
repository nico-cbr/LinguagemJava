public class Funcionario {
  private String nome;
  private int idade;
  private int salario;

  // getters
  public String getNome() {
    return nome;
  }

  public int getIdade() {
    return idade;
  }

  public int getSalario() {
    return salario;
  }

  // setters

  public void setNome(String nome) {
    this.nome = nome;
  }

  public void setIdade(int idade) {
    this.idade = idade;
  }

  public void setSalario(int salario) {
    this.salario = salario;
  }

}